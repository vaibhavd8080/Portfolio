-- 1. Create the Table
CREATE TABLE Sales (
    SaleID INT PRIMARY KEY,
    ProductName VARCHAR(50),
    Category VARCHAR(50),
    Quantity INT,
    PricePerUnit DECIMAL(10, 2),
    SaleDate DATE
);

