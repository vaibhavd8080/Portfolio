-- 3. DATA ANALYSIS QUERIES --

-- Q1: What is the Total Revenue generated by the store?
SELECT SUM(Quantity * PricePerUnit) AS Total_Revenue 
FROM Sales;

-- Q2: Which Category sells the most items?
SELECT Category, SUM(Quantity) AS Total_Items_Sold
FROM Sales
GROUP BY Category
ORDER BY Total_Items_Sold DESC;

-- Q3: Find all transactions where the sale amount was greater than 10,000
SELECT ProductName, (Quantity * PricePerUnit) AS Bill_Amount
FROM Sales
WHERE (Quantity * PricePerUnit) > 10000;
